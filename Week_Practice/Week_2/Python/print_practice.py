# 1. 기본 출력
print("Hello, World!")
print("파이썬 출력문 연습 중입니다.")

# 2. 숫자 출력
print(10)
print(3.14)

# 3. 문자열 + 숫자 (콤마 사용)
print("나이는", 24, "살입니다.")

# 4. 문자열 합치기 ( + 연산자 )
print("안녕하세요, " + "반갑습니다!")

# 5. 줄바꿈 없이 출력 (end 옵션)
print("줄바꿈 없이", end=" → ")
print("출력됩니다.")

# 6. 여러 줄 문자열 출력
print("""여러 줄 문자열입니다.
줄을 바꿔서도 출력됩니다.
이렇게 세 줄로 나올 수 있어요.""")

# 7. 이스케이프 문자 사용
print("큰따옴표 출력: \"Hello\"")
print("탭 간격\t연습")
print("줄바꿈\n연습")

# 8. 포맷팅 출력 (기초 f-string)
name = "혜림"
age = 24
print(f"{name}의 나이는 {age}살입니다.")
